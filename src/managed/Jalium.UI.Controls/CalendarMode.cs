namespace Jalium.UI.Controls;

/// <summary>
/// Specifies the display mode of a <see cref="Calendar"/>.
/// </summary>
public enum CalendarMode
{
    /// <summary>
    /// Displays the days of a month.
    /// </summary>
    Month,

    /// <summary>
    /// Displays the months of a year.
    /// </summary>
    Year,

    /// <summary>
    /// Displays the years of a decade.
    /// </summary>
    Decade,
}
