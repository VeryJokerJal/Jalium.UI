namespace Jalium.UI.Media.Animation;

// WPF declares the shared dependency properties on AnimationTimeline, but its
// CLR properties are emitted only on the concrete animation types that support
// composition. Keep these wrappers concrete so DeclaredOnly reflection matches
// WPF and unsupported AnimationTimeline<T> subclasses do not acquire the API.

public sealed partial class ByteAnimation
{
    public bool IsAdditive
    {
        get => (bool)GetValue(IsAdditiveProperty)!;
        set => SetValue(IsAdditiveProperty, value);
    }

    public bool IsCumulative
    {
        get => (bool)GetValue(IsCumulativeProperty)!;
        set => SetValue(IsCumulativeProperty, value);
    }
}

