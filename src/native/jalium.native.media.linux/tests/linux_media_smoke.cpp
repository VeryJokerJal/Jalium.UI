int main(int argc, char** argv)
{
    const bool expect_no_gstreamer =
        argc > 1 && std::strcmp(argv[1], "--expect-no-gstreamer") == 0;
    const int media_argument = expect_no_gstreamer ? 2 : 1;

    auto status = jalium_media_initialize();
    if (status != JALIUM_MEDIA_OK) return Fail("initialize", status);

    // The descriptor release API is intentionally idempotent: managed and
    // renderer teardown paths can converge after an exception. Prove that the
    // producer lifetime is released exactly once and the descriptor is reset.
    uint32_t lifetime_release_count = 0;
    jalium_video_decoder_gpu_descriptor_t synthetic_gpu{};
    for (auto& plane : synthetic_gpu.planes) plane.fd = -1;
    synthetic_gpu.acquire_fence_fd = -1;
    synthetic_gpu.lifetime_context = static_cast<uint64_t>(
        reinterpret_cast<uintptr_t>(&lifetime_release_count));
    synthetic_gpu.lifetime_release_callback = static_cast<uint64_t>(
        reinterpret_cast<uintptr_t>(&CountLifetimeRelease));
    jalium_video_decoder_release_gpu_surface_descriptor(&synthetic_gpu);
    jalium_video_decoder_release_gpu_surface_descriptor(&synthetic_gpu);
    if (lifetime_release_count != 1 || synthetic_gpu.lifetime_context != 0 ||
        synthetic_gpu.lifetime_release_callback != 0) {
        return Fail("GPU descriptor lifetime release",
                    JALIUM_MEDIA_E_DECODE_FAILED);
    }

    jalium_image_t image{};
    status = jalium_image_decode_memory(
        kPng, sizeof(kPng), JALIUM_PF_BGRA8, &image);
    if (status != JALIUM_MEDIA_OK) return Fail("PNG decode", status);
    if (image.width != 1 || image.height != 1 || !image.pixels) {
        std::fprintf(stderr, "PNG decode returned an invalid image\n");
        return 1;
    }
    jalium_image_free(&image);

    status = jalium_image_decode_memory(
        kPngWithRecoverableIdatCrcMismatch,
        sizeof(kPngWithRecoverableIdatCrcMismatch),
        JALIUM_PF_RGBA8, &image);
    if (status != JALIUM_MEDIA_OK) {
        return Fail("recoverable PNG CRC decode", status);
    }
    if (image.width != 1 || image.height != 1 || !image.pixels ||
        image.pixels[0] != 0xff || image.pixels[1] != 0xff ||
        image.pixels[2] != 0xff || image.pixels[3] != 0xff) {
        std::fprintf(stderr, "recoverable PNG CRC decode returned invalid pixels\n");
        return 1;
    }
    jalium_image_free(&image);

    if (expect_no_gstreamer) {
        if (jalium_media_supported_video_codecs() != 0) {
            std::fprintf(stderr, "disabled GStreamer reported video codecs\n");
            return 1;
        }

        // A URI must bypass the local fopen/FileExists guard. With the loader
        // disabled it fails synchronously as an unavailable capability rather
        // than being misreported as a missing local file.
        jalium_video_decoder_t* uri_decoder = nullptr;
        status = jalium_video_decoder_open_file(
            "https://example.invalid/video.mp4", JALIUM_PF_BGRA8,
            &uri_decoder);
        if (status != JALIUM_MEDIA_E_NOT_IMPLEMENTED || uri_decoder) {
            return Fail("disabled GStreamer URI status", status);
        }
    }

    uint32_t animated_count = 0;
    status = jalium_image_read_frame_count(
        kGif, sizeof(kGif), &animated_count);
    if (status != JALIUM_MEDIA_OK || animated_count != 2) {
        return Fail("GIF frame count", status);
    }
    for (uint32_t frame_index = 0; frame_index < animated_count; ++frame_index) {
        uint32_t delay_ms = 0;
        status = jalium_image_decode_frame(
            kGif, sizeof(kGif), frame_index, JALIUM_PF_BGRA8,
            &image, &delay_ms);
        const uint32_t expected_delay = frame_index == 0 ? 50 : 100;
        if (status != JALIUM_MEDIA_OK || image.width != 1 ||
            image.height != 1 || !image.pixels || delay_ms != expected_delay) {
            return Fail("GIF frame decode", status);
        }
        jalium_image_free(&image);
    }

    animated_count = 0;
    status = jalium_image_read_frame_count(
        kApng, sizeof(kApng), &animated_count);
    if (expect_no_gstreamer) {
        if (status != JALIUM_MEDIA_E_NOT_IMPLEMENTED) {
            return Fail("disabled GStreamer APNG status", status);
        }
    } else {
#if JALIUM_TEST_HAS_GSTREAMER
        if (status != JALIUM_MEDIA_OK || animated_count != 2) {
            return Fail("APNG frame count", status);
        }
        uint32_t delay_ms = 0;
        status = jalium_image_decode_frame(
            kApng, sizeof(kApng), 1, JALIUM_PF_BGRA8,
            &image, &delay_ms);
        if (status != JALIUM_MEDIA_OK || delay_ms != 250 ||
            !image.pixels || image.pixels[0] != 0xff ||
            image.pixels[1] != 0x00 || image.pixels[2] != 0x00 ||
            image.pixels[3] != 0xff) {
            return Fail("APNG second frame", status);
        }
        jalium_image_free(&image);
#endif
    }

    animated_count = 0;
    status = jalium_image_read_frame_count(
        kAnimatedWebP, sizeof(kAnimatedWebP), &animated_count);
    if (status != JALIUM_MEDIA_OK || animated_count != 2) {
        return Fail("animated WebP frame count", status);
    }
    for (uint32_t frame_index = 0; frame_index < animated_count; ++frame_index) {
        uint32_t delay_ms = 0;
        status = jalium_image_decode_frame(
            kAnimatedWebP, sizeof(kAnimatedWebP), frame_index,
            JALIUM_PF_BGRA8, &image, &delay_ms);
        const uint32_t expected_delay = frame_index == 0 ? 10 : 300;
        const bool expected_red = frame_index == 0;
        if (status != JALIUM_MEDIA_OK || image.width != 2 ||
            image.height != 2 || !image.pixels ||
            delay_ms != expected_delay ||
            image.pixels[1] > 4 || image.pixels[3] != 0xff ||
            (expected_red
                ? image.pixels[0] > 4 || image.pixels[2] < 250
                : image.pixels[0] < 250 || image.pixels[2] > 4)) {
            std::fprintf(
                stderr,
                "animated WebP frame %u: %ux%u delay=%u pixel=%02x,%02x,%02x,%02x\n",
                frame_index, image.width, image.height, delay_ms,
                image.pixels ? image.pixels[0] : 0,
                image.pixels ? image.pixels[1] : 0,
                image.pixels ? image.pixels[2] : 0,
                image.pixels ? image.pixels[3] : 0);
            return Fail("animated WebP frame decode", status);
        }
        jalium_image_free(&image);
    }

    status = jalium_audio_initialize();
    if (status != JALIUM_MEDIA_OK) return Fail("audio initialize", status);
    jalium_audio_decoder_t* wav = nullptr;
    status = jalium_audio_decoder_open_memory(
        kWav, sizeof(kWav), JALIUM_ACODEC_WAV, &wav);
    if (status != JALIUM_MEDIA_OK || !wav) return Fail("WAV open", status);
    jalium_audio_info_t wav_info{};
    status = jalium_audio_decoder_get_info(wav, &wav_info);
    if (status != JALIUM_MEDIA_OK || wav_info.sample_rate != 8000 ||
        wav_info.channels != 1 || wav_info.codec != JALIUM_ACODEC_WAV) {
        jalium_audio_decoder_close(wav);
        return Fail("WAV info", status);
    }
    float wav_frames[2]{};
    uint32_t wav_frames_read = 0;
    status = jalium_audio_decoder_read_frames(
        wav, wav_frames, 2, &wav_frames_read);
    if (status != JALIUM_MEDIA_OK || wav_frames_read == 0) {
        jalium_audio_decoder_close(wav);
        return Fail("WAV read", status);
    }
    jalium_audio_decoder_close(wav);
    jalium_audio_shutdown();

    jalium_video_decoder_t* decoder = nullptr;
    status = jalium_video_decoder_open_file(
        "/definitely/not/a/jalium-video.mp4", JALIUM_PF_BGRA8, &decoder);
    if (status != JALIUM_MEDIA_E_IO || decoder != nullptr) {
        return Fail("missing video", status);
    }

    if (argc > media_argument &&
        TestVideoSource(argv[media_argument], "video open", true) != 0) {
        return 1;
    }

    if (argc > media_argument + 1) {
        status = jalium_audio_initialize();
        if (status != JALIUM_MEDIA_OK) return Fail("audio initialize", status);
        jalium_audio_decoder_t* audio = nullptr;
        status = jalium_audio_decoder_open_file(
            argv[media_argument + 1], JALIUM_ACODEC_AAC, &audio);
        if (status != JALIUM_MEDIA_OK || !audio) return Fail("AAC open", status);
        jalium_audio_info_t info{};
        status = jalium_audio_decoder_get_info(audio, &info);
        if (status != JALIUM_MEDIA_OK || info.sample_rate == 0 ||
            info.channels == 0 || info.codec != JALIUM_ACODEC_AAC) {
            jalium_audio_decoder_close(audio);
            return Fail("AAC info", status);
        }
        std::vector<float> pcm(static_cast<size_t>(info.channels) * 1024);
        uint32_t frames = 0;
        status = jalium_audio_decoder_read_frames(
            audio, pcm.data(), 1024, &frames);
        if (status != JALIUM_MEDIA_OK || frames == 0) {
            jalium_audio_decoder_close(audio);
            return Fail("AAC read", status);
        }
        status = jalium_audio_decoder_seek_us(audio, 0);
        if (status != JALIUM_MEDIA_OK) {
            jalium_audio_decoder_close(audio);
            return Fail("AAC seek", status);
        }
        jalium_audio_decoder_close(audio);
        jalium_audio_shutdown();
    }

    if (argc > media_argument + 2) {
        status = jalium_audio_initialize();
        if (status != JALIUM_MEDIA_OK) return Fail("audio initialize", status);
        const int result = TestTrackAndSubtitleSource(
            argv[media_argument + 2]);
        jalium_audio_shutdown();
        if (result != 0) return result;
    }

    if (argc > media_argument + 3) {
        if (TestVideoSource(argv[media_argument + 3], "HTTP video open", true) !=
            0) {
            return 1;
        }
        status = jalium_audio_initialize();
        if (status != JALIUM_MEDIA_OK) return Fail("audio initialize", status);
        const int result = TestTrackAndSubtitleSource(
            argv[media_argument + 3]);
        jalium_audio_shutdown();
        if (result != 0) return result;
    }

    const bool advertised_rgb_dmabuf =
        (jalium_linux_media_capabilities() &
         JALIUM_LINUX_MEDIA_DMABUF_EXPORT) != 0;
    if (advertised_rgb_dmabuf != g_observed_renderable_rgb_dmabuf) {
        return Fail("DMABUF capability requires an observed renderable export",
                    JALIUM_MEDIA_E_DECODE_FAILED);
    }

    jalium_camera_device_t* devices = nullptr;
    uint32_t count = 0;
    status = jalium_camera_enumerate(&devices, &count);
    if (status != JALIUM_MEDIA_OK &&
        status != JALIUM_MEDIA_E_NOT_IMPLEMENTED) {
        return Fail("camera enumerate", status);
    }
    jalium_camera_devices_free(devices, count);

    jalium_camera_source_t* missing_camera = nullptr;
    status = jalium_camera_open(
        "__jalium_missing_camera__", 640, 480, 30.0,
        JALIUM_PF_BGRA8, &missing_camera);
#if JALIUM_TEST_HAS_GSTREAMER
    const auto expected_missing_camera_status = expect_no_gstreamer
        ? JALIUM_MEDIA_E_NOT_IMPLEMENTED : JALIUM_MEDIA_E_NO_DEVICE;
#else
    const auto expected_missing_camera_status = JALIUM_MEDIA_E_NO_DEVICE;
#endif
    if (status != expected_missing_camera_status || missing_camera) {
        if (missing_camera) jalium_camera_close(missing_camera);
        return Fail("missing camera open", status);
    }

    jalium_microphone_device_t* microphones = nullptr;
    count = 0;
    status = jalium_microphone_enumerate(&microphones, &count);
    if (status != JALIUM_MEDIA_OK &&
        status != JALIUM_MEDIA_E_NOT_IMPLEMENTED) {
        return Fail("microphone enumerate", status);
    }
    jalium_microphone_devices_free(microphones, count);

    jalium_microphone_source_t* missing_microphone = nullptr;
    status = jalium_microphone_open(
        "__jalium_missing_microphone__", 48000, 1, &missing_microphone);
#if JALIUM_TEST_HAS_GSTREAMER
    const auto expected_missing_microphone_status = expect_no_gstreamer
        ? JALIUM_MEDIA_E_NOT_IMPLEMENTED : JALIUM_MEDIA_E_NO_DEVICE;
#else
    const auto expected_missing_microphone_status =
        JALIUM_MEDIA_E_NOT_IMPLEMENTED;
#endif
    if (status != expected_missing_microphone_status || missing_microphone) {
        if (missing_microphone) jalium_microphone_close(missing_microphone);
        return Fail("missing microphone open", status);
    }

    jalium_media_shutdown();

    // A fresh lifecycle must be able to reuse (or deliberately decline) the
    // optional capability. GStreamer itself remains process-lifetime once
    // loaded; gst_deinit/dlclose is not safe while plugin modules retain refs.
    status = jalium_media_initialize();
    if (status != JALIUM_MEDIA_OK) return Fail("reinitialize", status);
    jalium_media_shutdown();
    return 0;
}
