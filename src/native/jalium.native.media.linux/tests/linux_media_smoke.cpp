int Fail(const char* message, jalium_media_status_t status)
{
    std::fprintf(stderr, "%s: %d (%s)\n", message, static_cast<int>(status),
                 jalium_media_status_string(status));
    return 1;
}

void CountLifetimeRelease(uint64_t context)
{
    auto* count = reinterpret_cast<uint32_t*>(
        static_cast<uintptr_t>(context));
    if (count) ++*count;
}

bool LanguageMatches(
    const char* value, const char* iso_639_1, const char* iso_639_2)
{
    return value &&
        (std::strcmp(value, iso_639_1) == 0 ||
         std::strcmp(value, iso_639_2) == 0);
}

int TestVideoSource(const char* source, const char* label, bool expect_h264)
{
    jalium_video_decoder_t* decoder = nullptr;
    auto status = jalium_video_decoder_open_file(
        source, JALIUM_PF_BGRA8, &decoder);
    if (status != JALIUM_MEDIA_OK || !decoder) return Fail(label, status);

    jalium_video_info_t info{};
    status = jalium_video_decoder_get_info(decoder, &info);
    if (status != JALIUM_MEDIA_OK || info.width == 0 || info.height == 0 ||
        info.duration_seconds <= 0 ||
        (expect_h264 && info.active_codec != JALIUM_CODEC_H264)) {
        jalium_video_decoder_close(decoder);
        return Fail("video info/codec", status);
    }

    // A VA-capable machine may expose the first sample as dma-buf; a software
    // CI machine reports NOT_IMPLEMENTED. Both paths must remain usable and
    // the GPU descriptor must carry complete multi-plane/lifetime metadata.
    jalium_video_decoder_gpu_descriptor_t gpu{};
    int64_t gpu_pts = 0;
    int32_t gpu_keyframe = 0;
    status = jalium_video_decoder_read_gpu_frame_descriptor(
        decoder, &gpu, &gpu_pts, &gpu_keyframe);
    if (status == JALIUM_MEDIA_OK) {
        if (gpu.kind != JALIUM_VS_KIND_LINUX_DMABUF || gpu.plane_count != 1 ||
            !IsRenderableRgbFourcc(gpu.drm_fourcc) ||
            gpu.lifetime_context == 0 ||
            gpu.lifetime_retain_callback == 0 ||
            gpu.lifetime_release_callback == 0) {
            jalium_video_decoder_release_gpu_surface_descriptor(&gpu);
            jalium_video_decoder_close(decoder);
            return Fail("GPU descriptor metadata", JALIUM_MEDIA_E_DECODE_FAILED);
        }
        for (uint32_t i = 0; i < gpu.plane_count; ++i) {
            if (gpu.planes[i].fd < 0 || gpu.planes[i].stride_bytes == 0) {
                jalium_video_decoder_release_gpu_surface_descriptor(&gpu);
                jalium_video_decoder_close(decoder);
                return Fail("GPU descriptor plane", JALIUM_MEDIA_E_DECODE_FAILED);
            }
        }
        g_observed_renderable_rgb_dmabuf = true;
        if ((jalium_linux_media_capabilities() &
             JALIUM_LINUX_MEDIA_DMABUF_EXPORT) == 0) {
            jalium_video_decoder_release_gpu_surface_descriptor(&gpu);
            jalium_video_decoder_close(decoder);
            return Fail("DMABUF capability after real RGB export",
                        JALIUM_MEDIA_E_DECODE_FAILED);
        }
        jalium_video_decoder_release_gpu_surface_descriptor(&gpu);
        status = jalium_video_decoder_disable_gpu_output(decoder);
        if (status != JALIUM_MEDIA_OK) {
            jalium_video_decoder_close(decoder);
            return Fail("GPU to CPU fallback", status);
        }
    } else if (status != JALIUM_MEDIA_E_NOT_IMPLEMENTED) {
        jalium_video_decoder_close(decoder);
        return Fail("GPU descriptor probe", status);
    }

    jalium_video_frame_t frame{};
    status = jalium_video_decoder_read_frame(decoder, &frame);
    if (status != JALIUM_MEDIA_OK || !frame.pixels || frame.width == 0) {
        jalium_video_decoder_close(decoder);
        return Fail("video frame", status);
    }

    constexpr int64_t seek_target_us = 900000;
    status = jalium_video_decoder_seek_microseconds(decoder, seek_target_us);
    if (status != JALIUM_MEDIA_OK) {
        jalium_video_decoder_close(decoder);
        return Fail("video accurate seek", status);
    }
    status = jalium_video_decoder_read_frame(decoder, &frame);
    if (status != JALIUM_MEDIA_OK || frame.pts_microseconds < 750000) {
        jalium_video_decoder_close(decoder);
        return Fail("video frame after accurate seek", status);
    }
    jalium_video_decoder_close(decoder);
    return 0;
}

int TestTrackAndSubtitleSource(const char* source)
{
    jalium_media_track_info_t* tracks = nullptr;
    uint32_t track_count = 0;
    auto status = jalium_media_discover_tracks(source, &tracks, &track_count);
    if (status != JALIUM_MEDIA_OK) return Fail("track discovery", status);
    uint32_t audio_count = 0;
    uint32_t subtitle_count = 0;
    bool english_audio = false;
    bool japanese_audio = false;
    for (uint32_t i = 0; i < track_count; ++i) {
        if (tracks[i].kind == JALIUM_MEDIA_TRACK_AUDIO) {
            ++audio_count;
            // GstDiscoverer normalizes ISO-639 metadata to either the
            // two-letter or three-letter form depending on plugin version.
            english_audio |= LanguageMatches(tracks[i].language, "en", "eng");
            japanese_audio |= LanguageMatches(tracks[i].language, "ja", "jpn");
        } else if (tracks[i].kind == JALIUM_MEDIA_TRACK_SUBTITLE) {
            ++subtitle_count;
        }
    }
    jalium_media_tracks_free(tracks, track_count);
    if (audio_count != 2 || subtitle_count != 1 || !english_audio ||
        !japanese_audio) {
        return Fail("track metadata", JALIUM_MEDIA_E_DECODE_FAILED);
    }

    jalium_audio_decoder_t* audio = nullptr;
    status = jalium_linux_audio_decoder_open_track(source, 1, &audio);
    if (status != JALIUM_MEDIA_OK || !audio) return Fail("audio track 1", status);
    jalium_audio_info_t audio_info{};
    status = jalium_audio_decoder_get_info(audio, &audio_info);
    std::vector<float> samples(
        static_cast<size_t>(std::max(1u, audio_info.channels)) * 1024);
    uint32_t frames = 0;
    if (status == JALIUM_MEDIA_OK) {
        status = jalium_audio_decoder_read_frames(
            audio, samples.data(), 1024, &frames);
    }
    if (status == JALIUM_MEDIA_OK && frames > 0) {
        status = jalium_audio_decoder_seek_us(audio, 900000);
    }
    if (status == JALIUM_MEDIA_OK) {
        frames = 0;
        status = jalium_audio_decoder_read_frames(
            audio, samples.data(), 1024, &frames);
    }
    jalium_audio_decoder_close(audio);
    if (status != JALIUM_MEDIA_OK || frames == 0) {
        return Fail("selected audio track read", status);
    }

    jalium_subtitle_decoder_t* subtitle = nullptr;
    status = jalium_subtitle_decoder_open(source, 0, &subtitle);
    if (status != JALIUM_MEDIA_OK || !subtitle) {
        return Fail("subtitle open", status);
    }
    jalium_subtitle_cue_t cue{};
    status = jalium_subtitle_decoder_read_cue(subtitle, &cue);
    if (status != JALIUM_MEDIA_OK || !cue.utf8_text ||
        std::string(cue.utf8_text).find("Jalium subtitle smoke") ==
            std::string::npos ||
        cue.start_microseconds < 200000 || cue.duration_microseconds < 900000) {
        jalium_subtitle_decoder_close(subtitle);
        return Fail("subtitle cue", status);
    }
    status = jalium_subtitle_decoder_seek_us(subtitle, 1250000);
    if (status == JALIUM_MEDIA_OK) {
        status = jalium_subtitle_decoder_read_cue(subtitle, &cue);
    }
    if (status == JALIUM_MEDIA_OK &&
        (!cue.utf8_text ||
         std::string(cue.utf8_text).find("Second cue") == std::string::npos ||
         cue.start_microseconds < 1250000)) {
        status = JALIUM_MEDIA_E_DECODE_FAILED;
    }
    jalium_subtitle_decoder_close(subtitle);
    return status == JALIUM_MEDIA_OK ? 0 : Fail("subtitle seek/read", status);
}

