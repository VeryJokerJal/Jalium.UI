                     "OnCanExecuteCopy", "OnCanExecuteDelete", "OnCopyingRowClipboardContent",
                     "OnExecutedBeginEdit", "OnExecutedCancelEdit", "OnExecutedCommitEdit",
                     "OnExecutedCopy", "OnExecutedDelete"
                 })
        {
            Assert.True(typeof(DataGrid).GetMethods(BindingFlags.Instance | BindingFlags.NonPublic | BindingFlags.DeclaredOnly)
                .Single(method => method.Name == name).IsVirtual);
        }

        foreach (var name in new[]
                 {
                     "OnColumnDisplayIndexChanged", "OnColumnHeaderDragCompleted", "OnColumnHeaderDragDelta",
                     "OnColumnHeaderDragStarted", "OnColumnReordered", "OnColumnReordering"
                 })
        {
            var method = typeof(DataGrid).GetMethods(
                    BindingFlags.Instance | BindingFlags.NonPublic | BindingFlags.DeclaredOnly)
                .Single(candidate => candidate.Name == name);
            Assert.True(method.IsVirtual);
            Assert.True(method.IsFamilyOrAssembly);
        }

        Assert.Equal(typeof(EventHandler<AddingNewItemEventArgs>),
            typeof(DataGrid).GetEvent(nameof(DataGrid.AddingNewItem))!.EventHandlerType);
        Assert.Equal(typeof(EventHandler<DragStartedEventArgs>),
            typeof(DataGrid).GetEvent(nameof(DataGrid.ColumnHeaderDragStarted))!.EventHandlerType);
        Assert.Equal(typeof(EventHandler<DragDeltaEventArgs>),
            typeof(DataGrid).GetEvent(nameof(DataGrid.ColumnHeaderDragDelta))!.EventHandlerType);
