        }
    }

    [Fact]
    public void HeaderAndRowDetailsConverters_ImplementWpfTemplateBehavior()
    {
        var culture = CultureInfo.InvariantCulture;
        Assert.Equal(Visibility.Visible, DataGrid.HeadersVisibilityConverter.Convert(
            DataGridHeadersVisibility.All, typeof(Visibility), DataGridHeadersVisibility.Column, culture));
        Assert.Equal(Visibility.Collapsed, DataGrid.HeadersVisibilityConverter.Convert(
            DataGridHeadersVisibility.Row, typeof(Visibility), DataGridHeadersVisibility.Column, culture));
        Assert.Equal(SelectiveScrollingOrientation.Vertical, DataGrid.RowDetailsScrollingConverter.Convert(
            true, typeof(SelectiveScrollingOrientation), SelectiveScrollingOrientation.Vertical, culture));
        Assert.Equal(SelectiveScrollingOrientation.Both, DataGrid.RowDetailsScrollingConverter.Convert(
            false, typeof(SelectiveScrollingOrientation), SelectiveScrollingOrientation.Vertical, culture));
    }

    private static void AssertField<TDeclaring, TField>(string name)
    {
        var field = typeof(TDeclaring).GetField(name, BindingFlags.Public | BindingFlags.Static)!;
        Assert.Equal(typeof(TField), field.FieldType);
    }

    private static void AssertProperty<TDeclaring, TProperty>(string name)
    {
        var property = typeof(TDeclaring).GetProperty(name, BindingFlags.Public | BindingFlags.Instance)!;
        Assert.Equal(typeof(TProperty), property.PropertyType);
        Assert.True(property.GetMethod!.IsPublic);
        Assert.True(property.SetMethod!.IsPublic);
    }

    private static void AssertMethod(
        Type declaringType,
        string name,
        Type returnType,
        bool isPublic,
        params Type[] parameterTypes)
    {
        var method = declaringType.GetMethod(
            name,
            BindingFlags.Public | BindingFlags.NonPublic | BindingFlags.Static | BindingFlags.Instance |
            BindingFlags.DeclaredOnly,
            binder: null,
            parameterTypes,
            modifiers: null)!;
        Assert.Equal(returnType, method.ReturnType);
        Assert.Equal(isPublic, method.IsPublic);
    }

