    [Fact]
    public void DataGrid_DeclaresRemainingWpfMethodsAndEvents()
    {
        AssertMethod(typeof(DataGrid), nameof(DataGrid.BeginEdit), typeof(bool), isPublic: true,
            typeof(RoutedEventArgs));
        AssertMethod(typeof(DataGrid), nameof(DataGrid.ColumnFromDisplayIndex), typeof(DataGridColumn),
            isPublic: true, typeof(int));
        AssertMethod(typeof(DataGrid), nameof(DataGrid.GenerateColumns), typeof(Collection<DataGridColumn>),
            isPublic: true, typeof(IItemProperties));
        AssertMethod(typeof(DataGrid), nameof(DataGrid.ScrollIntoView), typeof(void), isPublic: true,
            typeof(object), typeof(DataGridColumn));

        foreach (var name in new[]
                 {
                     "OnAddingNewItem", "OnAutoGeneratedColumns", "OnAutoGeneratingColumn",
