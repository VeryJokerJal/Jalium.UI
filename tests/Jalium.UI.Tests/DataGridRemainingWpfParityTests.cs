        Assert.Equal(typeof(ItemsControl), typeof(DataGridColumnHeadersPresenter).BaseType);
        Assert.True(typeof(VirtualizingPanel).IsAssignableFrom(typeof(DataGridCellsPanel)));

        foreach (var presenter in new[] { typeof(DataGridCellsPresenter), typeof(DataGridColumnHeadersPresenter) })
