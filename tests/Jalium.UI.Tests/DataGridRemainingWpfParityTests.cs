        Assert.False(typeof(DataGridRowHeader).GetProperty(nameof(DataGridRowHeader.IsRowSelected))!.SetMethod!.IsPublic);
        Assert.NotNull(PrimitiveColumnHeader.ColumnFloatingHeaderStyleKey);
        Assert.NotNull(PrimitiveColumnHeader.ColumnHeaderDropSeparatorStyleKey);

