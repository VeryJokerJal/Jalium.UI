    [Fact]
    public void GenerateColumns_UsesReportedItemMetadataAndDisplayIndexesAreValidated()
    {
        var descriptor = new object();
        var properties = new TestItemProperties(
        [
            new ItemPropertyInfo("Name", typeof(string), descriptor),
            new ItemPropertyInfo("Enabled", typeof(bool?), descriptor)
        ]);

        var columns = DataGrid.GenerateColumns(properties);
        Assert.Collection(columns,
            column => Assert.IsType<DataGridTextColumn>(column),
            column =>
            {
                var checkBox = Assert.IsType<DataGridCheckBoxColumn>(column);
                Assert.True(checkBox.IsThreeState);
            });
        Assert.All(columns, column => Assert.True(column.IsAutoGenerated));

        var grid = new DataGrid();
        grid.Columns.Add(columns[0]);
        grid.Columns.Add(columns[1]);
        Assert.Same(columns[0], grid.ColumnFromDisplayIndex(0));
        Assert.Same(columns[1], grid.ColumnFromDisplayIndex(1));
        Assert.Throws<ArgumentOutOfRangeException>(() => grid.ColumnFromDisplayIndex(-1));
        Assert.Throws<ArgumentOutOfRangeException>(() => grid.ColumnFromDisplayIndex(2));
    }

