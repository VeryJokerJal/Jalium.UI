    [Fact]
    public void TextAndComboColumns_ReadEditAndWriteThroughTheirBindings()
    {
        var model = new EditableRow { Name = "before", Choice = "two" };
        var textColumn = new ProbeTextColumn { Binding = new Binding(nameof(EditableRow.Name)) };
        var display = Assert.IsType<TextBlock>(textColumn.GenerateDisplay(model));
        Assert.Equal("before", display.Text);

        var textEditor = Assert.IsType<TextBox>(textColumn.GenerateEditor(model));
        textEditor.DataContext = model;
        Assert.Equal("before", textEditor.Text);
        textEditor.Text = "after";
        Assert.True(textColumn.Commit(textEditor));
        Assert.Equal("after", model.Name);

        var comboColumn = new ProbeComboColumn
        {
            ItemsSource = new[] { "one", "two" },
            SelectedItemBinding = new Binding(nameof(EditableRow.Choice))
        };
        var comboEditor = Assert.IsType<ComboBox>(comboColumn.GenerateEditor(model));
        comboEditor.DataContext = model;
        Assert.Equal("two", comboEditor.SelectedItem);
        comboEditor.SelectedItem = "one";
        Assert.True(comboColumn.Commit(comboEditor));
        Assert.Equal("one", model.Choice);
    }

    [Fact]
    public void ClipboardAndEventArgs_HaveWpfValueAndInheritanceSemantics()
    {
        var item = new object();
        var column = new DataGridTextColumn();
        var first = new DataGridClipboardCellContent(item, column, "value");
        var second = new DataGridClipboardCellContent(item, column, "value");
        Assert.Equal(first, second);
        Assert.True(first == second);
        Assert.False(first != second);
        Assert.Equal(first.GetHashCode(), second.GetHashCode());

        var rowArgs = new DataGridRowClipboardEventArgs(item, 0, 0, false);
        Assert.IsType<List<DataGridClipboardCellContent>>(rowArgs.ClipboardRowContent);

        Assert.Equal(typeof(EventArgs), typeof(DataGridColumnEventArgs).BaseType);
        Assert.Equal(typeof(DataGridColumnEventArgs), typeof(DataGridColumnReorderingEventArgs).BaseType);
        Assert.Equal(typeof(DataGridColumnEventArgs), typeof(DataGridSortingEventArgs).BaseType);
        Assert.Equal(typeof(EventArgs), typeof(DataGridPreparingCellForEditEventArgs).BaseType);

        var descriptor = new object();
        var autoArgs = new DataGridAutoGeneratingColumnEventArgs(column, "Name", typeof(string), descriptor);
        Assert.Same(descriptor, autoArgs.PropertyDescriptor);
    }

    [Fact]
